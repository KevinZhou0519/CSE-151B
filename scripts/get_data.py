# CELL 3 — Boxed-answer span finder and top-level comma splitter
#
# find_last_boxed_span():
#   Scans a response string and returns (start, end, content) for the LAST
#   \boxed{...} block, correctly handling nested braces and escaped characters.
#   Returns None if no complete \boxed{} is found.
#
# split_top_level_commas():
#   Splits a comma-separated string only at commas that are NOT inside braces.
#   Used to handle multi-answer boxes like \boxed{a, b, c}.
# ─────────────────────────────────────────────────────────────────────────────

def find_last_boxed_span(text: str):
    """Return (start, end, content) of the last complete \\boxed{...}, or None."""
    if not text:
        return None
    needle = r"\boxed"
    n, last, i = len(text), None, 0
    while True:
        idx = text.find(needle, i)
        if idx == -1:
            break
        j = idx + len(needle)
        while j < n and text[j].isspace():
            j += 1
        if j >= n or text[j] != "{":
            i = idx + len(needle)
            continue
        depth, k = 1, j + 1
        start_content = k
        while k < n and depth > 0:
            ch = text[k]
            if ch == "\\" and k + 1 < n:
                k += 2
                continue
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
                if depth == 0:
                    break
            k += 1
        if depth == 0:
            last = (idx, k + 1, text[start_content:k])
            i = k + 1
        else:
            i = idx + len(needle)
    return last


def split_top_level_commas(s: str):
    """Split on commas that are not inside any brace or paren level.
    Tracks both {} and () so that \left(...,... \right) is kept intact."""
    out, depth, start, i, n = [], 0, 0, 0, len(s)
    while i < n:
        ch = s[i]
        if ch == "\\" and i + 1 < n:
            i += 2
            continue
        if ch in "{(":
            depth += 1
        elif ch in "})":
            depth -= 1
        elif ch == "," and depth == 0:
            out.append(s[start:i])
            start = i + 1
        i += 1
    out.append(s[start:])
    return [p.strip() for p in out if p.strip() != ""]

def split_quoted_answers(content: str):
    """Split on commas OUTSIDE double quotes.
    Returns list of unquoted sub-answer strings, or None if no quotes found.

    "ans1","ans2"       -> ['ans1', 'ans2']
    "(-8,\\infty)"    -> ['(-8,\\infty)']   (comma inside quotes kept)
    "A,B,C","YES"       -> ['A,B,C', 'YES']
    no quotes here      -> None
    """
    content = content.strip()
    if '"' not in content:
        return None
    parts, current, in_quote = [], [], False
    for ch in content:
        if ch == '"':
            in_quote = not in_quote
        elif ch == ',' and not in_quote:
            part = ''.join(current).strip()
            if part:
                parts.append(part)
            current = []
        else:
            current.append(ch)
    part = ''.join(current).strip()
    if part:
        parts.append(part)
    return parts if parts else None


def compress_letter_list(s: str) -> str:
    """If s is comma-separated single letters like 'A, B, C', join to 'ABC'.
    Otherwise return s unchanged.
    Collapses multi-select MCQ answers written as 'A,B,C' into 'ABC'."""
    parts = [p.strip() for p in s.split(',')]
    if len(parts) > 1 and all(re.fullmatch(r'[A-Za-z]', p) for p in parts):
        return ''.join(p.upper() for p in parts)
    return s


def numerify_paren_list(s: str, n_digits: int = N_DIGITS) -> str:
    """Numerify a sub-answer that may be a parenthesised value list.
    Handles both bare '(val1, val2)' and LaTeX '\left(val1, val2\right)'.
    Each value inside is numerified independently.
    Otherwise passes the whole string to evaluate_piece()."""
    inner = s.strip()
    # Strip \left / \right wrappers so we can detect the '(' ... ')' structure.
    unwrapped = re.sub(r"\\left\s*", "", inner)
    unwrapped = re.sub(r"\\right\s*", "", unwrapped).strip()
    if unwrapped.startswith('(') and unwrapped.endswith(')'):
        inner_content = unwrapped[1:-1]
        parts = split_top_level_commas(inner_content)
        if len(parts) > 1:
            new_parts = [evaluate_piece(p, n_digits) for p in parts]
            return '(' + ', '.join(new_parts) + ')'
    return evaluate_piece(s, n_digits)

