def get_judger():
    """Import and instantiate the competition Judger (judger.py must be on sys.path)."""
    if JUDGER_DIR not in sys.path:
        sys.path.insert(0, JUDGER_DIR)
    try:
        from judger import Judger
    except Exception as e:
        sys.exit(
            f"[error] could not import judger.py ({type(e).__name__}: {e}).\n"
            "        Run this notebook from the competition starter-code "
            "directory, or set JUDGER_DIR to the folder holding judger.py."
        )
    return Judger(strict_extract=False)


def score_mcq(response: str, gold_letter: str) -> bool:
    """MCQ: True iff the extracted letter matches the gold letter (case-insensitive)."""
    return extract_letter(response) == str(gold_letter).strip().upper()


def judge_free(judger, numerified_text: str, gold) -> bool:
    """Free-form: True iff judger.auto_judge() accepts the numerified answer.

    For quoted-format responses (\\boxed{"ans1","ans2"}), the quotes are
    stripped and the parts are rejoined bare so that auto_judge()'s own
    split_by_comma() handles boundary detection — it tracks () brackets, so
    commas inside intervals like (-8,inf) are safe.

    Falls back to passing the text unchanged for legacy bare-comma responses.
    """
    gold_list = gold if isinstance(gold, list) else [gold]

    # Quoted format: strip quotes and reconstruct bare boxed for auto_judge
    span = find_last_boxed_span(numerified_text)
    if span is not None:
        start, end, content = span
        quoted_parts = split_quoted_answers(content)
        if quoted_parts is not None:
            new_box = "\\boxed{" + ",".join(quoted_parts) + "}"
            numerified_text = numerified_text[:start] + new_box + numerified_text[end:]

    try:
        return bool(judger.auto_judge(
            pred=numerified_text,
            gold=gold_list,
            options=[[]] * len(gold_list),
        ))
    except Exception:
        return False

# ─────────────────────────────────────────────────────────────────────────────
# CELL 7 — Answer key loader
#
# _detect_field():
#   Scans ALL rows of the answer key to find which column name is in use
#   from a priority list of candidates. Scanning all rows (not just row 0)
#   is important because optional fields (e.g. 'options') are absent from
#   free-form rows and would be missed by a single-row probe.
#
# load_answer_key():
#   Reads the public JSONL file into a dict keyed by question_id.
#   Each entry stores the question text, options (if MCQ), gold answer,
#   and an is_mcq flag. Handles both list and string gold answers.
# ─────────────────────────────────────────────────────────────────────────────

def _detect_field(rows: list, candidates, required=True, what=""):
    """Return the first candidate column name found in any row, or exit."""
    for c in candidates:
        if any(isinstance(r, dict) and c in r for r in rows):
            return c
    if required:
        all_keys = sorted({k for r in rows for k in r.keys()})
        sys.exit(
            f"[error] could not find the {what} field in the answer key. "
            f"Keys present: {all_keys}. "
            f"Add the right name to the KEY_*_FIELDS list in the Config cell."
        )
    return None


def load_answer_key(path: str) -> dict:
    """Load and index the ground-truth answer key from a JSONL file."""
    if not os.path.exists(path):
        sys.exit(
            f"[error] ANSWER_KEY_PATH not found: {path}\n"
            f"        Edit ANSWER_KEY_PATH in the Config cell."
        )
    rows = [json.loads(l) for l in open(path, encoding="utf-8") if l.strip()]
    if not rows:
        sys.exit(f"[error] answer key is empty: {path}")

    # Auto-detect column names from the priority lists defined in Config
    f_id  = _detect_field(rows, KEY_ID_FIELDS,     True,  "question-id")
    f_q   = _detect_field(rows, KEY_QUESTION_FLDS, True,  "question-text")
    f_ans = _detect_field(rows, KEY_ANSWER_FIELDS, True,  "answer")
    f_opt = _detect_field(rows, KEY_OPTION_FIELDS, False, "options")
    print(f"[answer key] id='{f_id}'  question='{f_q}'  "
          f"answer='{f_ans}'  options='{f_opt}'")

    key = {}
    for r in rows:
        qid  = str(r.get(f_id))
        opts = r.get(f_opt) if f_opt else None
        if opts and not isinstance(opts, list):
            opts = None   # ignore malformed option fields
        key[qid] = {
            "question": r.get(f_q, ""),
            "options":  opts,
            "answer":   r.get(f_ans),  # str or list
            "is_mcq":   bool(opts),
        }
    print(f"[answer key] loaded {len(key)} questions from {path}")
    return key

# ─────────────────────────────────────────────────────────────────────────────
# CELL 8 — System prompts and user-turn builder
#
# SYSTEM_PROMPT_MATH:
#   Instructs the model to output an EXACT symbolic closed-form answer inside
#   a single \boxed{} — never a decimal approximation. Defines Category 1
#   (discrete integers) vs Category 2 (continuous / pure math) formatting rules.
#
# SYSTEM_PROMPT_MCQ:
#   Ultra-short prompt for MCQ: emit exactly \boxed{X} and nothing else.
#
# build_prompt():
#   Returns (system_prompt, user_message) for a question.
#   For MCQ questions the options are appended to the user turn with letter
#   labels (A, B, C, …) matching the competition format.
# ─────────────────────────────────────────────────────────────────────────────

SYSTEM_PROMPT_MATH = r"""You are an expert mathematician. Reason as deeply as needed, then output ONLY the final boxed answer — no explanation, no working, no preamble.

## Classify each sub-answer before writing it

**Category 1 — Discrete count.** The answer counts INDIVISIBLE whole items (people, animals, coins, votes, cars, books, eggs, pellets…). Test: "Can 0.5 of this thing exist?" If NO → Category 1.

**Category 2 — Everything else.** Pure math (integrals, sums, limits) AND continuous physical quantities (temperature, time, distance, mass, money, probability, angle, area, concentration). Anything that can be fractional or irrational.

## Format

**Category 1 → INTEGER.**
- For Category 1 ONLY, you must evaluate to a number (because an integer is required).
- **Ask: does rounding DOWN fail to meet the requirement?**
  - **YES → CEILING (round up).** Use when the integer is a MINIMUM that must be fully satisfied: sample sizes, number of buses/containers/boxes needed, minimum cuts required, etc.
    - e.g. sample size 1324.79 → **1325** (can't survey 0.79 of a person)
    - e.g. buses for 314 people (50 seats each) → ceil(314/50) = **7**
  - **NO → FLOOR (round down).** Use when the integer counts COMPLETED whole items: complete cycles, whole years elapsed, items fully produced, etc.
    - e.g. complete batches from 314.76 → **314**
- **Exception:** If the problem explicitly says "round to the nearest whole number" → standard rounding (314.76 → 315, 314.4 → 314).
- Never output a decimal, fraction, or symbolic form.

**Category 2 → EXACT SYMBOLIC CLOSED-FORM EXPRESSION. NEVER a decimal.**
- Output a single closed-form expression in valid LaTeX (e.g. `50 \cdot (2/5)^{5/3}`, `\frac{6 \ln 5}{\ln(5/2)}`, `\frac{\pi^2}{6}`, `200 e^{-3/2}`, `25 \ln 2`).
- KEEP π, e, ln, log, √, sin, cos, tan, arcsin, arccos, arctan, fractional exponents, and exact fractions as symbols.
- **CRITICAL: Even if the problem says "give N decimal places", "use at least N significant digits", or "evaluate numerically" — IGNORE that instruction and output the exact symbolic form.** The grader handles decimal conversion.
- If a problem coefficient is a decimal, convert it to an exact fraction first (e.g. 4.76 → 119/25, 0.9658 → 4829/5000), then use that fraction inside the symbolic expression.
- `\arctan(119/25)` is a valid symbolic answer. `1.3637` is NOT.
- `\pi` is a valid symbolic answer. `3.1416` is NOT.
- Do NOT evaluate to a decimal. No `\approx`. No scientific notation.
- An integer or exact rational is fine when that's the natural answer (e.g. `441`, `\frac{12800}{29}`).
- Simplify when easy (combine logs, reduce fractions, cancel exponents) but do not approximate.

## Output rules

1. Emit EXACTLY ONE `\boxed{...}` and nothing else before or after it.
2. Wrap EVERY sub-answer in double quotes `"..."` inside the box.
3. **Multiple subquestions** (the problem asks for part a, part b, etc.) → separate quoted strings:
   `\boxed{"ans1","ans2","ans3"}`
4. **Multiple values for ONE answer** (e.g. all solutions to an equation, all values of i) → wrap all values in `(...)` comma-separated, inside ONE quoted string:
   `\boxed{"(val1, val2, val3)"}`
5. Cat 1 → plain integer. Cat 2 → symbolic closed-form (never decimal).

## Examples

Single answer:           `\boxed{"211250"}` or `\boxed{"\frac{\pi^2}{6}"}` or `\boxed{"200e^{-3/2}"}`
Interval answer:         `\boxed{"(-8,\infty)"}`
Multiple solutions:      `\boxed{"(0, -\frac{11}{8})"}` or `\boxed{"(\frac{1}{3}, -2)"}`
Multiple values of i:    `\boxed{"(2^{-1/8}-1, -2^{-1/8}+1)"}`
Multi-subquestion:       `\boxed{"\arctan(123/31)","e^2"}` or `\boxed{"(50(2/5)^{5/3},50(2/13)^{4/3})","\frac{6\ln5}{\ln(5/2)}"}`
Multi-select MCQ:        `\boxed{"B, C, E, G"}`
Single letter MCQ:       `\boxed{"C"}`
"""

SYSTEM_PROMPT_MCQ = (
    "You are an expert mathematician. Reason as deeply as needed"
    "CRITICAL: Your ENTIRE response must be exactly one token: \\boxed{X} where X is the correct letter."
    " No explanation. No working. No preamble. Just \\boxed{X}."
    " Example of a complete valid response: \\boxed{C}"
    " If your computed answer matches no option, pick the closest. Still output only \\boxed{X}."
)



def build_prompt(question: str, options: Optional[list]):
    """Return (system_prompt, user_message) matching the inference-time format.

    For MCQ questions the option list is rendered with letter labels
    A, B, C, … and appended to the question text.
    """
    if options:
        labels    = [chr(65 + i) for i in range(len(options))]
        opts_text = "\n".join(f"{lbl}. {str(opt).strip()}"
                              for lbl, opt in zip(labels, options))
        return SYSTEM_PROMPT_MCQ, f"{question}\n\nOptions:\n{opts_text}"
    return SYSTEM_PROMPT_MATH, question

# ─────────────────────────────────────────────────────────────────────────────
# CELL 9 — Qwen3-Thinking assistant-turn formatter
#
# build_assistant():
#   Wraps the teacher model's raw solution text as a Qwen3-4B-Thinking
#   assistant turn:
#
#       <think>
#       {reasoning text, with its trailing \boxed{} unwrapped}
#       </think>
#
#       \boxed{final_answer}
#
#   Key decisions:
#   - The ORIGINAL symbolic boxed answer is used (not the numerified version).
#     Numerification was only needed for the judger; the SFT target must be
#     the exact symbolic form the model should learn to produce.
#   - The trailing \boxed{} is unwrapped from the <think> body so that the
#     only real \boxed{} in the turn is the explicit answer after </think>.
#   - DeepSeek output has no <think> tags; we add them because Qwen3-4B-
#     Thinking is trained to separate chain-of-thought from the final answer.
# ─────────────────────────────────────────────────────────────────────────────

def build_assistant(gemini_response: str, final_boxed: str) -> str:
    """Format a correct teacher trace as a Qwen3-Thinking assistant turn.

    Args:
        gemini_response: The full raw teacher output (reasoning + answer).
        final_boxed:     The ORIGINAL symbolic content of the last \\boxed{}
                         (not numerified), used as the SFT answer target.

    Returns:
        A string of the form:
            <think>\n{reasoning}\n</think>\n\n\\boxed{final_boxed}
    """
    body = gemini_response.strip()

    # Remove the trailing \boxed{} from the reasoning body so it isn't
    # duplicated — the explicit \boxed{} after </think> is the canonical one.
    span = find_last_boxed_span(body)
    if span is not None:
        start, end, content = span
        body = (body[:start] + content + body[end:]).strip()

    return f"<think>\n{body}\n</think>\n\n\\boxed{{{final_boxed}}}"

