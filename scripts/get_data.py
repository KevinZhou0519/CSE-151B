# CELL 4 — LaTeX cleaning and numerical evaluation (numerification)
#
# _CLEANUPS / _clean_latex():
#   Strip LaTeX macros that confuse sympy (\dfrac → \frac, spacing commands,
#   \left / \right, etc.) before parsing.
#
# evaluate_piece():
#   Convert a single boxed sub-answer to an N-digit decimal string using
#   sympy's LaTeX parser (or sympify fallback). Plain integers and floats are
#   handled cheaply without sympy. Returns the original string if evaluation
#   fails so nothing is silently dropped.
#
# numerify_response():
#   Locate the LAST \boxed{} in a full response, split its comma-separated
#   parts, numerify each one, and splice the result back into the text.
#   The judger then compares these decimal strings to the gold answers.
#   NOTE: the ORIGINAL symbolic text (not the numerified version) is what
#   gets saved into the SFT dataset — numerification is grading-only.
# ─────────────────────────────────────────────────────────────────────────────

# Regex substitutions applied before feeding LaTeX to sympy.
_CLEANUPS = [
    (r"\\dfrac", r"\\frac"),
    (r"\\tfrac", r"\\frac"),
    (r"\\!",     ""),
    (r"\\,",     ""),
    (r"\\;",     ""),
    (r"\\ ",     ""),
    (r"\\\\ ",  " "),
    (r"\\left",  ""),
    (r"\\right", ""),
    (r"\\$",     ""),
    (r"\\%",     ""),    # strip LaTeX \% — the fraction already IS the percentage value
    (r"\\text\{([^}]*)\}", r"\1"),  # \text{C} → C
]

import math as _math

def _sub_stat_quantiles(s: str) -> str:
    """Pre-substitute statistical distribution quantiles with decimal values.

    Replaces tokens like z_{0.015}, chi^2_{0.01,1}, t_{0.025,10},
    F_{0.05,2,30} with their numeric values BEFORE passing to parse_latex,
    so that surrounding expressions (e.g. '26 - \frac{\sqrt{105}}{15} z_{0.015}')
    can be evaluated as a whole rather than failing on free symbols.

    Also handles t with fractional df: t_{0.025, \frac{num}{den}} (Welch's df).

    Requires scipy; silently skips if not installed.
    """
    try:
        from scipy import stats as _sc

        # z_{alpha}  →  norm.ppf(1-alpha)
        def _z(m):
            return str(_sc.norm.ppf(1 - float(m.group(1))))
        s = re.sub(r"\bz\s*_\{?\s*([0-9.]+)\s*\}?", _z, s)

        # chi^2_{alpha, df}  or  \chi^2_{alpha, df}
        # Note: include optional leading \ so \chi^2_{...} doesn't leave a stray \.
        def _chi2(m):
            return str(_sc.chi2.ppf(1 - float(m.group(1)), int(m.group(2))))
        s = re.sub(
            r"\\?chi\^?2\s*_\{?\s*([0-9.]+)\s*,\s*([0-9]+)\s*\}?",
            _chi2, s, flags=re.IGNORECASE,
        )

        # t_{alpha, \frac{num}{den}}  — Welch's df expressed as a fraction
        def _t_frac(m):
            alpha = float(m.group(1))
            df = float(m.group(2)) / float(m.group(3))
            return str(_sc.t.ppf(1 - alpha, df))
        s = re.sub(
            r"\bt\s*_\{?\s*([0-9.]+)\s*,\s*\\frac\{([0-9]+)\}\{([0-9]+)\}\s*\}?",
            _t_frac, s,
        )

        # t_{alpha, df}  — integer or decimal df
        def _t(m):
            return str(_sc.t.ppf(1 - float(m.group(1)), float(m.group(2))))
        s = re.sub(r"\bt\s*_\{?\s*([0-9.]+)\s*,\s*([0-9.]+)\s*\}?", _t, s)

        # F_{alpha, df1, df2}
        def _f(m):
            return str(_sc.f.ppf(1 - float(m.group(1)), int(m.group(2)), int(m.group(3))))
        s = re.sub(
            r"\bF\s*_\{?\s*([0-9.]+)\s*,\s*([0-9]+)\s*,\s*([0-9]+)\s*\}?", _f, s
        )

    except ImportError:
        pass
    return s
def _eval_cdf_expression(s: str, n_digits: int, _depth: int = 0):
    r"""Evaluate expressions containing \Phi, \Phi^{-1}, \Pr(T_n < x).

    \Phi(x)        → norm.cdf(evaluate(x))
    \Phi^{-1}(p)   → norm.ppf(evaluate(p))
    \Pr(T_n < x)   → t.cdf(evaluate(x), df=n)

    Called from evaluate_piece; _depth prevents infinite recursion.
    Returns decimal string, or None if no CDF pattern found.
    """
    if "\\Phi" not in s and "\\Pr" not in s:
        return None
    try:
        from scipy import stats as _sc

        def _inner(expr_str):
            """Evaluate a sub-expression, guarded against recursion."""
            if _depth >= 2:
                return None
            result = evaluate_piece(expr_str.strip(), n_digits, _depth + 1)
            try:
                return float(result)
            except (ValueError, TypeError):
                return None

        s_work = s

        # \Phi^{-1}(p) → norm.ppf(p)  [check before \Phi(x) to avoid partial match]
        def _phi_inv(m):
            v = _inner(m.group(1))
            return f"{_sc.norm.ppf(v):.{n_digits}f}" if v is not None else m.group(0)
        s_work = re.sub(r"\\Phi\s*\^\{-1\}\s*\(([^)]+)\)", _phi_inv, s_work)

        # \Phi(x) → norm.cdf(x)
        def _phi(m):
            v = _inner(m.group(1))
            return f"{_sc.norm.cdf(v):.{n_digits}f}" if v is not None else m.group(0)
        s_work = re.sub(r"\\Phi\s*\(([^)]+)\)", _phi, s_work)

        # \Pr(T_{n} < x) → t.cdf(x, df=n)
        def _pr_t(m):
            df_v = _inner(m.group(1))
            x_v  = _inner(m.group(2))
            if df_v is None or x_v is None:
                return m.group(0)
            return f"{_sc.t.cdf(x_v, df_v):.{n_digits}f}"
        s_work = re.sub(
            r"\\Pr\s*\(\s*T_\{?(\d+)\}?\s*<\s*([^)]+)\)", _pr_t, s_work
        )

        if s_work == s:
            return None  # nothing matched

        # Try to evaluate the residual arithmetic (e.g. "1 - 0.8605...")
        try:
            return f"{float(s_work):.{n_digits}f}"
        except ValueError:
            pass
        try:
            val = sp.N(sp.sympify(s_work), WORK_PREC)
            return _format_numeric(val, n_digits)
        except Exception:
            pass
        return s_work   # best-effort: return modified string

    except Exception:
        return None

def _clean_latex(s: str) -> str:
    """Apply _CLEANUPS and strip a leading '=' sign (e.g. '= 3/4' → '3/4')."""
    s = s.strip()
    # Convert degree notation to a radian DECIMAL wrapped in braces.
    # Using a plain decimal (not \pi/180) avoids introducing '*' which is
    # not valid LaTeX and causes parse_latex to fail.
    #   \tan 23^\circ   →  \tan {0.40142...}   (braces = grouped argument)
    #   \cos(67^\circ)  →  \cos({1.16937...})
    #   67°             →  {1.16937...}
    def _deg2rad(m):
        return "{" + str(_math.radians(float(m.group(1)))) + "}"
    # (?(2)\}) = closing brace only if group 2 captured the opening brace.
    # This prevents eating the '}' that closes a surrounding \frac{}{...} argument.
    s = re.sub(r"(\d+(?:\.\d+)?)\s*\^(\{)?\\circ(?(2)\})", _deg2rad, s)
    s = re.sub(r"(\d+(?:\.\d+)?)\s*°",                      _deg2rad, s)
    for pat, rep in _CLEANUPS:
        s = re.sub(pat, rep, s)
    return re.sub(r"^=\s*", "", s).strip()

def _format_numeric(val, n_digits: int) -> str:
    """Format a sympy numeric value as a decimal string.
    Handles real, complex, and infinite results.
      real:     '3.141592653590'
      complex:  '-1.000000000000+2.449489742783i'
      infinity: '\\infty' or '-\\infty'
    Raises TypeError if val still contains free symbols.
    """
    re_part, im_part = val.as_real_imag()
    re_f = float(re_part)
    im_f = float(im_part)
    if _math.isinf(re_f):
        return "\\infty" if re_f > 0 else "-\\infty"
    if abs(im_f) < 1e-12:          # effectively real
        return f"{re_f:.{n_digits}f}"
    sign = "+" if im_f >= 0 else ""
    return f"{re_f:.{n_digits}f}{sign}{im_f:.{n_digits}f}i"

def evaluate_piece(piece: str, n_digits: int = N_DIGITS, _depth: int = 0) -> str:
    p = (piece or "").strip()
    if not p:
        return p
    # Single capital letter → always an MCQ choice, never a math expression.
    # Must come before sympy parsing because sympy maps E → Euler's constant.
    if re.fullmatch(r"[A-Z]", p):
        return p
    if re.fullmatch(r"-?\d+", p):
        return p
    m = re.fullmatch(r"-?\d+\.\d+", p)
    if m:
        try:
            return f"{float(p):.{n_digits}f}"
        except Exception:
            pass
    s = _clean_latex(p)
    s = _sub_stat_quantiles(s)   # replace z_{α}, chi²_{α,df}, etc. with decimals

    # Handle \Phi, \Phi^{-1}, \Pr(T_n < x) expressions
    cdf_result = _eval_cdf_expression(s, n_digits, _depth)
    if cdf_result is not None:
        return cdf_result

    try:
        if parse_latex is None:
            raise RuntimeError("no latex parser")
        expr = parse_latex(s)
        # Equality like x = -1/2 → extract RHS
        if isinstance(expr, sp.Eq):
            expr = expr.rhs
        expr = expr.subs([
            (sp.Symbol('pi'), sp.pi),
            (sp.Symbol('e'),  sp.E),
            (sp.Symbol('i'),  sp.I),   # treat bare i as imaginary unit
        ])
        val = sp.N(expr, WORK_PREC)
        return _format_numeric(val, n_digits)
    except Exception as e:
        print(f"[parse_latex FAILED] input={s!r}  error={e}")
    try:
        fallback_s = s.replace("\\", "")
        expr = sp.sympify(fallback_s)
        val  = sp.N(expr, WORK_PREC)
        return _format_numeric(val, n_digits)
    except Exception as e:
        print(f"[sympify FAILED] input={fallback_s!r}  error={e}")

    # Fallback 3: scipy for statistical distribution quantiles.
    # Handles chi^2_{alpha,df}, t_{alpha,df}, F_{alpha,df1,df2}, z_{alpha}.
    # Only reached when both sympy paths failed, so false-positives on
    # ambiguous letters (t, z) are unlikely in practice.
    try:
        from scipy import stats as _scipy_stats

        # chi^2_{alpha, df}  or  chi2_{alpha, df}
        m = re.search(
            r"chi\^?2\s*_\{?\s*([0-9.]+)\s*,\s*([0-9]+)\s*\}?", p, re.IGNORECASE
        )
        if m:
            alpha, df = float(m.group(1)), int(m.group(2))
            val = _scipy_stats.chi2.ppf(1 - alpha, df)
            return f"{val:.{n_digits}f}"

        # F_{alpha, df1, df2}
        m = re.search(
            r"[Ff]\s*_\{?\s*([0-9.]+)\s*,\s*([0-9]+)\s*,\s*([0-9]+)\s*\}?", p
        )
        if m:
            alpha, df1, df2 = float(m.group(1)), int(m.group(2)), int(m.group(3))
            val = _scipy_stats.f.ppf(1 - alpha, df1, df2)
            return f"{val:.{n_digits}f}"

        # t_{alpha, df}
        m = re.search(
            r"\bt\s*_\{?\s*([0-9.]+)\s*,\s*([0-9]+)\s*\}?", p
        )
        if m:
            alpha, df = float(m.group(1)), int(m.group(2))
            val = _scipy_stats.t.ppf(1 - alpha, df)
            return f"{val:.{n_digits}f}"

        # z_{alpha}  or  z_alpha
        m = re.search(r"\bz\s*_\{?\s*([0-9.]+)\s*\}?", p)
        if m:
            alpha = float(m.group(1))
            val = _scipy_stats.norm.ppf(1 - alpha)
            return f"{val:.{n_digits}f}"

    except Exception as e:
        print(f"[scipy FAILED] input={p!r}  error={e}")

    return p

def numerify_response(text: str, n_digits: int = N_DIGITS) -> str:
    """Replace the content of the LAST \\boxed{} with numerically evaluated parts.

    Supports two formats:
    - Quoted (new):  \\boxed{"ans1","ans2"} — each sub-answer in double quotes;
      compress_letter_list() collapses 'A,B,C' -> 'ABC' within each part;
      numerify_paren_list() handles '(val1, val2)' multi-value answers.
    - Legacy:        \\boxed{a, b, c} — bare comma-separated sub-answers.
    """
    try:
        span = find_last_boxed_span(text)
        if span is None:
            return text
        start, end, content = span

        # New: quoted format
        quoted_parts = split_quoted_answers(content)
        if quoted_parts is not None:
            new_parts = []
            for p in quoted_parts:
                p = compress_letter_list(p)        # 'A,B,C' -> 'ABC'
                p = numerify_paren_list(p, n_digits)  # evaluate numeric pieces
                new_parts.append(p)
            new_box = "\\boxed{" + ",".join(f'"{p}"' for p in new_parts) + "}"
            return text[:start] + new_box + text[end:]

        # Legacy: bare comma-separated (or single \left(...\right) interval)
        parts = split_top_level_commas(content)
        if not parts:
            return text
        new_parts = [numerify_paren_list(p, n_digits) for p in parts]
        new_box = "\\boxed{" + ", ".join(new_parts) + "}"
        return text[:start] + new_box + text[end:]
    except Exception:
        return text

