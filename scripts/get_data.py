# CELL 5 — Answer extraction helpers
#
# extract_letter():
#   For MCQ responses: pull the single letter from \boxed{X}.
#   Falls back to scanning for the last bare capital letter in the text
#   (handles responses that omit the box but write the letter).
#
# extract_boxed():
#   Return the raw content string inside the last \boxed{} (symbolic, not
#   numerified). Used to record the SFT answer target.
# ─────────────────────────────────────────────────────────────────────────────

def extract_letter(text: str) -> str:
    """Extract the single capital letter from \\boxed{X}, or from bare text."""
    m = re.search(r"\\boxed\{([A-Za-z])\}", text or "")
    if m:
        return m.group(1).upper()
    # Fallback: last bare capital letter in the response
    matches = re.findall(r"\b([A-Z])\b", (text or "").upper())
    return matches[-1] if matches else ""


def extract_boxed(text: str) -> str:
    """Return raw content of the last \\boxed{...} (symbolic, not evaluated)."""
    span = find_last_boxed_span(text or "")
    return span[2] if span else ""

