# ─────────────────────────────────────────────────────────────────────────────
# CELL 10 — Main rejection-sampling pipeline
#
# This cell ties everything together:
#   1. Creates OUT_DIR (deepseekv4-pro/) if it does not exist.
#   2. Loads the judger, answer key, and teacher model responses.
#   3. For each response:
#        a. Skip if unfinished (stop_reason != STOP) and REQUIRE_FINISHED=True.
#        b. Skip if the question_id has no entry in the answer key.
#        c. Skip if no \boxed{} answer found in the response.
#        d. Numerify the response, then judge with the official judger.
#        e. Skip duplicates (same qid + same response text).
#        f. If ONE_PER_QUESTION=True, skip extra correct attempts.
#        g. Build the SFT message dict and accumulate.
#   4. Writes all outputs into deepseekv4-pro/:
#        sft_data.jsonl      → messages only (plug into training loop)
#        kept_correct.jsonl  → full records for inspection
#        grading_report.txt  → counts + teacher accuracy
# ─────────────────────────────────────────────────────────────────────────────

def main():
    if not os.path.exists(DEEPSEEK_PATH):
        sys.exit(f"[error] DEEPSEEK_PATH not found: {DEEPSEEK_PATH}")

    # --- Step 0: ensure output directory exists ---
    os.makedirs(OUT_DIR, exist_ok=True)
    print(f"[output dir] {os.path.abspath(OUT_DIR)}")

    # --- Step 1: initialise judger and load data ---
    judger = get_judger()                    # competition's official grader
    key    = load_answer_key(ANSWER_KEY_PATH)

    gem = [json.loads(l) for l in open(DEEPSEEK_PATH, encoding="utf-8") if l.strip()]
    print(f"[responses]  loaded {len(gem)} responses from {DEEPSEEK_PATH}")

    stats = Counter()
    kept, wrong_kept, seen, kept_qids = [], [], set(), set()

    # --- Step 2: iterate and rejection-sample ---
    for rec in gem:
        stats["total"] += 1
        qid  = str(rec.get("question_id", rec.get("id")))
        resp = rec.get("response") or ""
        thinking = rec.get("thinking") or ""
        stop = rec.get("stop_reason", "")

        # (a) Drop unfinished responses — truncated traces have no usable \boxed{}
        if REQUIRE_FINISHED and stop.upper() != "STOP":
            stats["dropped_unfinished"] += 1
            continue

        # (b) Drop if question_id not in answer key
        info = key.get(qid)
        if info is None:
            stats["dropped_no_goldkey"] += 1
            continue

        # (c) Extract the ORIGINAL symbolic boxed answer (used for SFT target)
        pred_box = (rec.get("boxed") or "").strip() or extract_boxed(resp)
        print("this is the responds: " + resp)
        print("this is the pred_box: " + pred_box)

        if not pred_box:
            stats["dropped_no_box"] += 1
            continue

        # (d) Grade: numerify first, then run the official judger


        is_mcq = info["is_mcq"]
        if is_mcq:
            ok = score_mcq(resp, info["answer"])
        else:
            numerified = numerify_response(resp)
            print("this is the numerified: " + numerified)
            ok = judge_free(judger, numerified, info["answer"])
        if not ok:
          stats["dropped_incorrect"] += 1
          wrong_kept.append({
              "question_id": qid,
              "is_mcq":      is_mcq,
              "gold":        info["answer"],
              "pred_boxed":  pred_box,
              "stop_reason": stop,
              "response":    resp,
              "thinking":    thinking,
          })
          continue

        # (e) Deduplication: same question + same response text → skip
        attempt = rec.get("attempt", rec.get("attempt_id"))
        dedup_k = (qid, attempt)
        if dedup_k in seen:
            stats["dropped_duplicate"] += 1
            continue
        seen.add(dedup_k)

        # (f) If ONE_PER_QUESTION, keep only the first correct trace per qid
        if ONE_PER_QUESTION and qid in kept_qids:
            stats["dropped_extra_per_q"] += 1
            continue
        kept_qids.add(qid)

        # (g) Build the SFT example in Qwen3-Thinking chat format
        system, user = build_prompt(info["question"], info["options"])
        assistant = build_assistant(thinking, pred_box)
        kept.append({
            "question_id": qid,
            "is_mcq":      is_mcq,
            "gold":        info["answer"],
            "pred_boxed":  pred_box,
            "messages": [
                {"role": "system",    "content": system},
                {"role": "user",      "content": user},
                {"role": "assistant", "content": assistant},
            ],
        })
        stats["kept"] += 1
        stats["kept_mcq" if is_mcq else "kept_free"] += 1

    # --- Step 3: write all outputs into deepseekv4-pro/ ---

    # sft_data.jsonl — only the messages list (plug straight into a training loop)
    with open(OUT_SFT, "w", encoding="utf-8") as f:
        for k in kept:
            f.write(json.dumps({"messages": k["messages"]}, ensure_ascii=False) + "\n")

    # kept_correct.jsonl — full metadata for inspection / debugging
    with open(OUT_KEPT, "w", encoding="utf-8") as f:
        for k in kept:
            f.write(json.dumps(k, ensure_ascii=False) + "\n")
    # kept_wrong.jsonl — incorrect responses, same shape as kept_correct.jsonl
    with open(OUT_WRONG, "w", encoding="utf-8") as f:
        for w in wrong_kept:
            f.write(json.dumps(w, ensure_ascii=False) + "\n")

    # grading_report.txt — human-readable stats
    graded = (
        stats["total"]
        - stats["dropped_unfinished"]
        - stats["dropped_no_goldkey"]
        - stats["dropped_no_box"]
    )
    acc = (stats["kept"] / graded * 100) if graded else 0.0
    report = [
        "=== DeepSeek-V4-Pro grading report (official judger.Judger) ===",
        f"total responses           : {stats['total']}",
        f"  dropped (unfinished)    : {stats['dropped_unfinished']}",
        f"  dropped (no answer key) : {stats['dropped_no_goldkey']}",
        f"  dropped (no boxed)      : {stats['dropped_no_box']}",
        f"  dropped (incorrect)     : {stats['dropped_incorrect']}",
        f"  dropped (duplicate)     : {stats['dropped_duplicate']}",
        f"  dropped (extra per q)   : {stats['dropped_extra_per_q']}",
        f"gradeable responses       : {graded}",
        f"KEPT for SFT              : {stats['kept']}   "
        f"(free={stats['kept_free']}, mcq={stats['kept_mcq']})",
        f"teacher accuracy (kept/gradeable) : {acc:.1f}%",
        "",
        f"wrote: {OUT_SFT}  ({stats['kept']} training examples)",
        f"wrote: {OUT_KEPT}  (full info for inspection)",
        f"wrote: {OUT_WRONG}  ({len(wrong_kept)} incorrect responses)",
        f"wrote: {OUT_REPORT}",
    ]
    text = "\n".join(report)
    print("\n" + text)
    with open(OUT_REPORT, "w", encoding="utf-8") as f:
        f.write(text + "\n")

    if stats["kept"] == 0:
        print("\n[warning] 0 examples kept — check that the answer key joins on "
              "question_id and that judger.py imported correctly.")

main()

