def extract_math_record(ex, source_tag: str) -> dict | None:
    """Extract a single clean record from a MATH dataset example."""
    prob = (ex.get("problem") or "").strip()
    sol  = (ex.get("solution") or "").strip()
    if not prob or not sol: return None
    if is_proof_question(prob): return None

    # MATH solutions contain intermediate \boxed{} — always use the LAST one
    spans = find_all_boxed_spans(sol)
    if not spans: return None
    gt_raw = spans[-1][2].strip()
    if not gt_raw: return None

    # Universal quality filter
    clean, _ = answer_is_clean(gt_raw)
    if not clean: return None

    subj = (ex.get("type") or ex.get("subject") or "unknown") \
           .lower().replace(" ", "_")
    return {
        "problem":       prob,
        "options":       None,
        "gt_raw":        gt_raw,
        "gt_display":    format_gt_display(gt_raw),   # \\boxed{"answer"}
        "n_parts":       1,
        "btype":         "single",
        "reward_policy": "all_or_nothing",
        "source":        f"{source_tag}_{subj}",
        "level":         ex.get("level", ""),
    }

