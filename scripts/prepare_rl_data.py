import json, os

IN_FILE  = "/content/drive/MyDrive/CSE 151B/rl_data/rl_data_3000.jsonl"
OUT_FILE = "/content/drive/MyDrive/CSE 151B/rl_data/rl_data_3000.jsonl"  # overwrites in place

# ── IDs to delete — fill these in ─────────────────────────────────────────────
DELETE_IDS = {
    241,547,1154,1415,1811,2095,
}
# ──────────────────────────────────────────────────────────────────────────────

records = []
with open(IN_FILE, encoding="utf-8") as f:
    for line in f:
        if line.strip():
            rec = json.loads(line)
            if rec.get("id") not in DELETE_IDS:
                records.append(rec)

# Re-assign IDs so they stay 0-indexed and contiguous after deletion
for i, rec in enumerate(records):
    rec["id"] = i

with open(OUT_FILE, "w", encoding="utf-8") as f:
    for rec in records:
        f.write(json.dumps(rec, ensure_ascii=False) + "\n")

print(f"Deleted {len(DELETE_IDS)} record(s).  {len(records)} remain → {OUT_FILE}")

