# 1. Matrix / table layout in problem or answer
_MATRIX_RE = re.compile(
    r"\\begin\{(?:p|b|B|v|V|small)?matrix\*?\}"
    r"|\\begin\{(?:cases|array|tabular)\}"
    r"|\\pmatrix|\\bmatrix"
    r"|\\operatorname\{det\}|\\det\s*\{",
    re.IGNORECASE,
)

# 2. Unjudgeable answer patterns (checked on gt_raw parts only)
_ANS_BAD_RE = re.compile(
    r"\\pm|\\mp"                                        # ±  → two values at once
    r"|\\ldots|\\cdots|\\dots"                          # … → sequence / pattern
    r"|\\sum_|\\prod_|\\int(?:[_^]|\s*\\)"              # unresolved Σ / Π / ∫
    r"|\\forall|\\exists"                               # logic quantifiers
    r"|\\Rightarrow|\\Leftrightarrow|\\iff|\\implies"   # logical connectives
    r"|\\(?:long)?(?:right|left)?(?:to|mapsto|rightarrow|leftarrow)"  # f: x → y
    r"|\\\{"                                            # bare set \{1,2,3\}
    r"|\\cup|\\cap"                                     # union/intersection of sets
    r"|\\\\",                                           # double-backslash (broken LaTeX)
)

# 3. Problem needs visual context — Asymptote drawing blocks OR figure references
_VISUAL_RE = re.compile(
    r"\[asy\]"                                          # Asymptote drawing block
    r"|\b(?:figure|diagram|as\s+shown|the\s+graph\s+shows?|"
    r"see\s+(?:the\s+)?(?:figure|diagram|table|graph)|"
    r"the\s+table\s+(?:below|above|shows?)|shown\s+(?:below|above))\b",
    re.IGNORECASE,
)

# 4. True/False questions
_TRUEFALSE_RE = re.compile(r"\btrue\s+or\s+false\b|\bfalse\s+or\s+true\b",
                            re.IGNORECASE)

# 5. Bare comma-separated value list in a single gt_raw part
#    e.g. "0,1,2,3,4" or "20, 38, 380" — multiple values not wrapped in (...) or [...]
#    Allow: thousand-separator numbers like "30,951", LaTeX {,} separators.
def _is_bare_value_list(s: str) -> bool:
    s = s.strip()
    if not s: return False
    if s.startswith(("(", "[", "\\left", "\\{")): return False   # already grouped
    if re.fullmatch(r"[\d\s\{\,\.\-\+]+", s): return False       # plain number with commas
    depth = 0
    for ch in s:
        if ch in "({[": depth += 1
        elif ch in ")}]": depth -= 1
        elif ch == "," and depth == 0:
            return True
    return False


def _final_filter_reject(rec: dict) -> tuple[bool, str]:
    """Return (True, reason) if the record should be dropped."""
    problem  = rec.get("problem", "")
    gt_raw   = rec.get("gt_raw", "")
    gt_parts = gt_raw if isinstance(gt_raw, list) else [gt_raw]
    all_text = [problem, rec.get("gt_display", "")] + gt_parts

    if any(_MATRIX_RE.search(t) for t in all_text if t):
        return True, "matrix"
    if any(_ANS_BAD_RE.search(p) for p in gt_parts if p):
        return True, "bad_answer_pattern"
    if any(_is_bare_value_list(p) for p in gt_parts if p):
        return True, "bare_value_list"
    if _VISUAL_RE.search(problem):
        return True, "needs_visual"
    if _TRUEFALSE_RE.search(problem):
        return True, "true_false"

    return False, ""


