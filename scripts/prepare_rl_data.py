def find_all_boxed_spans(text: str):
    """Return list of (start, end, content) for every complete \\boxed{...}."""
    needle = r"\boxed"
    n, spans, i = len(text), [], 0
    while True:
        idx = text.find(needle, i)
        if idx == -1: break
        j = idx + len(needle)
        while j < n and text[j].isspace(): j += 1
        if j >= n or text[j] != "{":
            i = idx + len(needle); continue
        depth, k, sc = 1, j + 1, j + 1
        while k < n and depth > 0:
            ch = text[k]
            if ch == "\\" and k + 1 < n: k += 2; continue
            if ch == "{":   depth += 1
            elif ch == "}":
                depth -= 1
                if depth == 0: break
            k += 1
        if depth == 0:
            spans.append((idx, k + 1, text[sc:k]))
            i = k + 1
        else:
            i = idx + len(needle)
    return spans

# ── Multi-box case detector ───────────────────────────────────────────────────
_CASE_PATTERNS = [
    re.compile(r"\\text\s*\{\s*[Ss]\s*\d"),
    re.compile(r"\\text\s*\{[Cc]ase"),
    re.compile(r"\\text\s*\{[Ii]f\b"),
    re.compile(r"\\text\s*\{[Ww]hen\b"),
    re.compile(r"[a-z]\s*=\s*\d+\s*[,\\].*\\in\b"),
    re.compile(r"\\in\s*\\left"),
    re.compile(r"\\in\s*[\(\[]"),
]
_MAX_PART_LEN = 120

def _is_case_box(content: str) -> bool:
    c = content.strip()
    if len(c) > _MAX_PART_LEN: return True
    return any(p.search(c) for p in _CASE_PATTERNS)

def merge_boxes(solution: str):
    """Returns (merged_or_single_ans, n_boxes, btype).

    btype: 'none' | 'single' | 'subpart' | 'case' | 'mixed'
    Callers skip 'none', 'case', 'mixed'.
    """
    spans = find_all_boxed_spans(solution)
    if not spans:       return "", 0, "none"
    if len(spans) == 1: return spans[0][2].strip(), 1, "single"

    flags = [_is_case_box(s[2]) for s in spans]
    n_case = sum(flags)
    if n_case == len(spans): btype = "case"
    elif n_case > 0:         btype = "mixed"
    else:                    btype = "subpart"

    if btype in ("case", "mixed"):
        return "", len(spans), btype

    contents = [s[2].strip() for s in spans]
    merged   = "\\boxed{" + ",".join(f'"{c}"' for c in contents) + "}"
    return merged, len(spans), "subpart"

# ── Proof filter ──────────────────────────────────────────────────────────────
_PROOF_RE = re.compile(
    r"\b(prove|proof|show\s+that|demonstrate|verify\s+that|establish\s+that"
    r"|show\s+there\s+(exists|is)|find\s+all\s+.{0,40}\s+and\s+prove)\b",
    re.IGNORECASE,
)
def is_proof_question(p: str) -> bool:
    return bool(_PROOF_RE.search(p))

# ── gt_display formatter ───────────────────────────────────────────────────────
def format_gt_display(gt_raw) -> str:
    """Always produce \\boxed{"part1","part2"} with quotes around every part."""
    if isinstance(gt_raw, list):
        return "\\boxed{" + ",".join(f'"{p}"' for p in gt_raw) + "}"
    return f'\\boxed{{"{gt_raw}"}}'

# ── Universal answer-quality filter (all btypes) ──────────────────────────────
_CURRENCY_RE     = re.compile(r'\\[$]|[$€£¥]')
_MCQ_ARTIFACT_RE = re.compile(r'[A-D]\)')          # "C)" leaking into free-response
_DIGIT_WORD_RE   = re.compile(r'\d+\s+[a-zA-Z]{3,}')  # "14 right triangles", "8 km"

def answer_is_clean(gt_raw) -> tuple[bool, str]:
    """Return (True, 'ok') if every part of gt_raw is a clean math value.

    Rejects (returns False) if any part:
      - is empty / whitespace only           → 'empty_part'
      - contains '='  (equation, not value)  → 'has_equals'
      - contains currency ($, €, £ …)        → 'currency'
      - contains \\text{  (English label)    → 'text_label'
      - contains \\!  (forced spacing / time)→ 'forced_spacing'
      - matches [A-D])  (MCQ letter bleed)   → 'mcq_artifact'
      - matches digit+space+3+letters        → 'units_or_label'
        (catches "14 right triangles", "8 p.m.")
    """
    parts = gt_raw if isinstance(gt_raw, list) else [gt_raw]
    for p in parts:
        s = p.strip()
        if not s:                            return False, "empty_part"
        if "=" in s:                         return False, "has_equals"
        if _CURRENCY_RE.search(s):           return False, "currency"
        if r'\text{' in s:                   return False, "text_label"
        if r'\!' in s:                       return False, "forced_spacing"
        if _MCQ_ARTIFACT_RE.search(s):       return False, "mcq_artifact"
        if _DIGIT_WORD_RE.search(s):         return False, "units_or_label"
    return True, "ok"

# ── Multi-part quality filters (Rules 1–3) ────────────────────────────────────
_LONG_WORD_RE  = re.compile(r'[a-zA-Z]{5,}')   # 5+ consecutive letters
_WORD_SPACE_RE = re.compile(r'[a-zA-Z] [a-zA-Z]')  # letter-space-letter (prose)

# Rule 2: banned LaTeX constructs
_BANNED_LATEX_STRS = [
    r'\text{',
    r'\begin{cases}', r'\begin{array}', r'\begin{pmatrix}',
    r'\mid',
    r'\cup', r'\cap',
    r'\mathbb',
]

def _part_is_pure_math(s: str) -> bool:
    """Rule 1: True if s looks like a clean mathematical expression.

    Allows: digits, a-z letters, standard LaTeX commands ≤4 letters
            (\\frac, \\sqrt, \\sin, \\cos, \\pi, \\ln …), operators,
            grouping chars.
    Rejects: any word-like run of 5+ letters (catches \\theta, \\omega,
             \\alpha, English words), or letter-space-letter (prose).
    """
    if _LONG_WORD_RE.search(s):
        return False
    if _WORD_SPACE_RE.search(s):
        return False
    return True

def _part_has_banned_latex(s: str) -> bool:
    """Rule 2: True if s contains layout/set/text LaTeX we want to exclude."""
    if "|" in s:       # catches both \mid and bare | (set-builder notation)
        return True
    return any(b in s for b in _BANNED_LATEX_STRS)

_MAX_PART_CHARS = 30   # Rule 3: single-part length cap
_MAX_PARTS      = 3    # Rule 3: max number of sub-answers

def multipart_passes_filters(contents: list[str]) -> tuple[bool, str]:
    """Apply Rules 1–3 to a list of sub-answer strings.

    Returns (passes: bool, reason: str).
    """
    # Rule 3a: too many parts
    if len(contents) > _MAX_PARTS:
        return False, f"too_many_parts({len(contents)})"
    for part in contents:
        # Rule 3b: individual part too long
        if len(part) > _MAX_PART_CHARS:
            return False, f"part_too_long({len(part)})"
        # Rule 1
        if not _part_is_pure_math(part):
            return False, "not_pure_math"
        # Rule 2
        if _part_has_banned_latex(part):
            return False, "banned_latex"
    return True, "ok"

